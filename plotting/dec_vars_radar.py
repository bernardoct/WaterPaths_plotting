def plot_decvars_radar(dec_vars, min_max):
    n_utils = len(dec_vars)
    n_dec_vars = len(dec_vars.items()[0][1])
    n_sols = len(dec_vars.items()[0][1].items()[0][1])
    spoke_labels = dec_vars.items()[0][1].keys()

    theta = radar_factory(n_dec_vars, frame='circle')
    fig, axes = plt.subplots(2, n_utils / 2, subplot_kw=dict(projection='radar'))
    axes = axes.ravel()

    for utility_name, ax in zip(dec_vars.keys(), axes):
        ax.set_rgrids([0.2, 0.4, 0.6, 0.8])
        ax.set_title(utility_name, weight='bold', size='medium',
                     position=(0.5, 1.1),
                     horizontalalignment='center', verticalalignment='center')
        for s in range(n_sols):
            data_sol = []
            for dv_name, c in zip(dec_vars[utility_name].keys(),
                                  cm.get_cmap('Accent').colors):
                data_sol.append(dec_vars[utility_name][dv_name][s])
            ax.plot(theta, data_sol, color=c)
            ax.fill(theta, data_sol, facecolor=c, alpha = 0.25)
        ax.plot(theta, min_max[0], color='white', alpha=0.0)
        ax.plot(theta, min_max[1], color='white', alpha=0.0)
        ax.set_varlabels(spoke_labels)

